first_person_pronouns = ["i", "me", "my", "mine", "we", "us", "our"]
emotional_words = ["happy", "sad", "help", "understand", "care", "love", "support", "listen", "comfort"]

# scoring functions
def balanced_anthro_score(response):
    words = response.lower().split()
    pronoun_count = sum(1 for word in words if word in first_person_pronouns)
    emotion_count = sum(1 for word in words if word in emotional_words)
    sentiment = TextBlob(response).sentiment.polarity
    score = pronoun_count * 2 + emotion_count * 3 + sentiment * 5
    return round(score, 2)

def balanced_intell_score(response):
    response_length = len(response.split())
    capped_length = min(response_length, 60)  # Cap at 60 words
    sentiment = TextBlob(response).sentiment.polarity
    score = (capped_length * 0.7) + (sentiment * 5)  # Balanced influence
    return round(score, 2)

# Applying the scoring functions to all responses
balanced_scored_responses = []

for resp in agent_responses:
    balanced_scored_responses.append({
        'Response': resp,
        'AnthroScore': balanced_anthro_score(resp),
        'IntellScore': balanced_intell_score(resp)
    })

# Converting results to DataFrame and sort
df_balanced_scores = pd.DataFrame(balanced_scored_responses)
df_balanced_scores_sorted = df_balanced_scores.sort_values(by='AnthroScore', ascending=False).reset_index(drop=True)

